    private void anteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_anteriorActionPerformed
        // TODO add your handling code here:
        if (contador > 0){
            display (contador - 1);
            contador --;
        }
    }//GEN-LAST:event_anteriorActionPerformed

    private void posteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_posteriorActionPerformed
        // Si no estás en el último
        if (contador < personas.size()-1){
            display (contador + 1);
            contador ++;
        }
    }//GEN-LAST:event_posteriorActionPerformed

    private void primeroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_primeroActionPerformed
        // si hay algún elemento
        if (personas.size() != 0){
            display (0);
            contador = 0;
        }
    }//GEN-LAST:event_primeroActionPerformed

    private void ultimoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ultimoActionPerformed
        // TODO add your handling code here:
        if (personas.size() != 0){
            display (personas.size()-1);
            contador = personas.size()-1;
        }
    }//GEN-LAST:event_ultimoActionPerformed

    private void annadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_annadirActionPerformed
        // TODO add your handling code here:
        String nombre = JOptionPane.showInputDialog("Nombre: ");
        String txt = JOptionPane.showInputDialog("Edad: ");
        int edad = Integer.parseInt (txt);
        String email = JOptionPane.showInputDialog("Email: ");
                
        Persona p = new Persona (nombre, edad, email);
        personas.add (p);
        
        JOptionPane.showMessageDialog (null, "Ańadido " + nombre + ", " + edad + " ańos");
        
        contador = personas.size()-1;
        display (personas.size()-1);
    }//GEN-LAST:event_annadirActionPerformed

    private void borrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarActionPerformed
        // TODO add your handling code here:
        if (personas.size() > 1) {
            personas.remove(contador);
            if (contador < personas.size()){
                display (contador);
            } else {
                display (personas.size()-1);
                contador = personas.size()-1;
            }
        } else if (personas.size() == 1){
            personas.remove(contador);
            txtNombre.setText("");
            txtEdad.setText ("0");
            txtEmail.setText("");
            contador = 0;
            indice.setText(contador + " de " + personas.size());
        } else {
            txtNombre.setText("");
            txtEdad.setText ("0");
            txtEmail.setText("");
        }
        
        
    }//GEN-LAST:event_borrarActionPerformed

